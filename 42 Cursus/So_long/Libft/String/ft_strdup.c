#include "../inc/libft.h"

char	*ft_strdup(const char *s)
{
	size_t	len;
	char	*new;

	len = ft_strlen(s);
	new = malloc((sizeof(char) * (len + 1)));
	if (!new)
		return (0);
	ft_strlcpy(new, s, (len + 1));
	return (new);
}
