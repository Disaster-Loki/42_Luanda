#include <unistd.h>

int	main(int av, char **args)
{
	int	i = 0;
	char	*str;
	str = args[1];
	while (av == 2 && str[i])
	{
		if (str[i] == '_')
			str[++i] -= 32;
		write(1, &str[i], 1);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
