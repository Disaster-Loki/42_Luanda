#include <unistd.h>

int	main(int av, char **args)
{
	int	i = 0;
	if (av == 2)
	{
		while (args[1][i])
			i++;
		while (i > 0)
			write(1, &args[1][--i], 1);
	}
	write(1, "\n", 1);
	return (0);
}
