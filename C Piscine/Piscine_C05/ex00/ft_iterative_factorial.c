int	ft_iterative_factorial(int nb)
{
	int	aux;

	aux = 1;
	while (nb > 1)
	{
		aux *= nb;
		nb--;
	}
	if (nb == 0 || nb == 1)
	{
		return (1);
	}
	if (nb <= 0)
	{
		return (0);
	}
	return (aux);
}
